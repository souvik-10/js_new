// while loop 

let index = 0
while (index <= 10) { // match the condition and execute according to it 
    // console.log(`value of index is ${index}`)
    index = index + 2
    
}

// while array example 

let myArray = ["batman", "superman", "deadpool"]

let array = 0
while (array < myArray.length) {
    // console.log(`value is ${myArray[array]}`)
    array = array + 1
    
    
}


